# Community Guidelines Script

# Function to display community guidelines
def display_guidelines():
    print("Welcome to Our Community!")
    print("Here are our community guidelines:\n")
    print("1. Respect other members.")
    print("2. No hate speech, bullying, or harassment of any kind.")
    print("3. Stay on topic and keep discussions relevant.")
    print("4. Avoid spamming or excessive self-promotion.")
    print("5. Use appropriate language and tone in all communications.")
    print("6. Report any issues or violations to the moderators.")
    print("7. Have fun and contribute positively to the community!")

# Main function to execute the script
def main():
    print("=== Community Guidelines ===\n")
    display_guidelines()

# Entry point of the script
if __name__ == "__main__":
    main()
